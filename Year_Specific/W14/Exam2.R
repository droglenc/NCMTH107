library(NCStats)
setwd("C:/aaaWork/Class Materials/MTH107/Year_Specific/W14")
d <- read.table("Exam2_Data.txt",header=TRUE)
str(d)
lm1 <- lm(abundance~year,data=d)
coef(lm1)
rSquared(lm1)
windows(4,3); par(mar=c(3,3,0.5,0.5),mgp=c(1.75,0.5,0),tcl=-0.2)
fitPlot(lm1,xlab="Year",ylab="Abundance")
legend("topright",c("Y=6384-3.16*X","r^2=0.533"),bty="n",cex=1.25)

lm2 <- lm(year~abundance,data=d)
coef(lm2)
rSquared(lm2)
fitPlot(lm2,ylab="Year",xlab="Abundance")
